package week5_lab;

public class exercise1 {
	public static void main(String[] args) {
		int number = 15;
		number = 30;
		System.out.println(number);
		
		double d = 4.5;
//		save 15 - 16 decimal point 
		System.out.println("Our double Number:");
		System.out.println(d);
		
//		if we want to use float you will have to cast it.
//		save 6 - 7 decimal point 
		System.out.println("Our float Number:");
		float f = (float) 4.5;
		System.out.println(f);
//		shorter way of casting float
		float f2 = (float) 4.5f;
		System.out.println(f2);

	  }
}
